package com.matrix.tech.models;

import java.io.Serializable;

public class Rangos implements Serializable {
	
	private String diez;
	private String veinte;
	private String treinta;
	private String cuarenta;
	private String cincuenta;
	private String sesenta;
	private String setenta;
	private String ochenta;

	public String getDiez() {
		return diez;
	}

	public void setDiez(String diez) {
		this.diez = diez;
	}

	public String getVeinte() {
		return veinte;
	}

	public void setVeinte(String veinte) {
		this.veinte = veinte;
	}

	public String getTreinta() {
		return treinta;
	}

	public void setTreinta(String treinta) {
		this.treinta = treinta;
	}

	public String getCuarenta() {
		return cuarenta;
	}

	public void setCuarenta(String cuarenta) {
		this.cuarenta = cuarenta;
	}

	public String getCincuenta() {
		return cincuenta;
	}

	public void setCincuenta(String cincuenta) {
		this.cincuenta = cincuenta;
	}

	public String getSesenta() {
		return sesenta;
	}

	public void setSesenta(String sesenta) {
		this.sesenta = sesenta;
	}

	public String getSetenta() {
		return setenta;
	}

	public void setSetenta(String setenta) {
		this.setenta = setenta;
	}

	public String getOchenta() {
		return ochenta;
	}

	public void setOchenta(String ochenta) {
		this.ochenta = ochenta;
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

}
